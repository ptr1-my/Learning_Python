n = float(input("Masukkan Nilai Siswa Untuk Mengecek Grade : "))

if n >= 90 < 100:
    print (f'Siswa Dengan Nilai : {n}\nMendapatkan Grade : A')
elif n >= 80 < 90:
    print (f'Siswa Dengan Nilai : {n}\nMendapatkan Grade : B')
elif n >= 70 < 80:
    print (f'Siswa Dengan Nilai : {n}\nMendapatkan Grade : C')
elif n >= 60 < 70:
    print (f'Siswa Dengan Nilai : {n}\nMendapatkan Grade : D')
elif n < 60:
    print (f'Siswa Dengan Nilai : {n}\nMendapatkan Grade : E')